export const translateTeamName = (englishName: string): string => {
  if (!englishName) return '';
  return countryTranslations[englishName] || englishName;
};

export const translateStatus = (status: string): string => {
  switch(status) {
    case 'SCHEDULED': return 'PLANLANDI';
    case 'IN_PLAY': return 'CANLI';
    case 'FINISHED': return 'BİTTİ';
    case 'PENDING': return 'BEKLENİYOR';
    case 'EVALUATED': return 'DEĞERLENDİRİLDİ';
    default: return status;
  }
};
